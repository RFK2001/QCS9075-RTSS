/*===========================================================================
                             FUNCTION PROTOTYPES
===========================================================================*/

/**
 * @brief Sets up UIE operation. For proper memory management,
 *        uie_deinit() should be called at end of life.
 *        Unwrapping the keys in this function.
 *
 * @param[in]  uie_img_ctx     The context we work on
 * @param[in]  header_address  The address of the encrypted image parameter section
 * @param[in]  crypto_ftbl     The cryptographic drivers
 *
 * @return - 0 on success, non-zero on failure.
 */
/* TBD: Check the Encryption enablement fuse in this library */
uint32_t uie_init(uie_ctx *uie_img_ctx,
                void *header_address,
                crypto_ftbl_type * crypto_ftbl);

/**
 * @brief Decrypts a segment in-place and verifies its validity.
 *
 * @param[in]  uie_img_ctx     The context we work on
 * @param[in]  segment_id      The segment to check
 * @param[in]  segment_address The address of the segment to decrypt
 * @param[in]  segment_length  The byte length of the segment
 * @param[out] digest          The optional SHA256 hashsum; a non-NULL
 *                             input address will result in a digest output
 * @param[in]  crypto_ftbl     The cryptographic drivers
 *
 * @return - 0 on success, non-zero on failure.
 */
uint32_t uie_decrypt_segment(uie_ctx *uie_img_ctx,
                           uint8_t segment_id,
                           void *segment_address, uint64_t segment_length,
                           crypto_ftbl_type * crypto_ftbl,
                           uint8_t *digest);

/**
 * @brief Tears down UIE operation.
 *
 * @param[in]  uie_img_ctx     The context to work on
 */
void uie_deinit(uie_ctx *uie_img_ctx);

/**
 * @brief return the byte size of uie param
 *
 */
uint32_t uie_get_param_size_c(uie_ctx *uie_img_ctx);
uint32_t uie_get_param_size(void); // to be depricated for new target

/**
 * @brief return boolean to show if the image is encrypted
 *
 * @param[in]  header_address  The address of the encrypted image parameter section
 *
 * @return - TRUE if image is encrypted, FALSE otherwise.
 */
bool uie_get_is_image_encrypted(void *header_address);

#endif /* UIE_PBL_H */
