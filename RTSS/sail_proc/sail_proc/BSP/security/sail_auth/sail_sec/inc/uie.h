#define UIE_ENC_PARAM_SEC_INFO_ID_MISMATCH 0x14
#define UIE_ENC_PARAM_SEC_KEY_TYPE_MISMATCH 0x15
#define UIE_ENC_PARAM_SEC_MODE_MISMATCH 0x16
#define UIE_ENC_PARAM_SEC_SIZE_MISMATCH 0x17
#define UIE_ENC_PARAM_SEC_ALGO_MISMATCH 0x18
#define UIE_ENC_STATUS_IMAGE_ENCRYPTION_DISABLED 0x19
#define UIE_STATUS_IMAGE_ENCRYPTION_DISABLED 0x1A
