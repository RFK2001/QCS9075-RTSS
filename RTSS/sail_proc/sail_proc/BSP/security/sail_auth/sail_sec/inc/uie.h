#define UIE_ENC_PARAM_SEC_ADATA_KEY_LEN_MISMATCH 0xB 
#define UIE_IMAGE_ENC_INVALID_L3_LEN_BYTE_0 0xC 
#define UIE_IMAGE_ENC_INVALID_L3_LEN_BYTE_1 0xD 
#define UIE_ENC_PARAM_SEC_MAGIC_MISTMATCH 0xE 
